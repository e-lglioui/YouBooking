
function loadDoc() {
    var xhttp = new XMLHttpRequest();
    xhttp.onreadystatechange = function() {
      if (this.readyState == 4 && this.status == 200) {
       document.getElementById("contenue").innerHTML = this.responseText;
      }
    };
    xhttp.open("GET", "e.php", true);
    xhttp.send();
  }

